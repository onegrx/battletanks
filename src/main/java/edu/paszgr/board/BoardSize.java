package edu.paszgr.board;

public class BoardSize {
    private int xSize;
    private int ySize;

    public BoardSize(int xSize, int ySize) {
        this.xSize = xSize;
        this.ySize = ySize;
    }

    public int getxSize() {
        return xSize;
    }

    public int getySize() {
        return ySize;
    }
}
